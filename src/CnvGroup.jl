struct CvGroupElement{N} <: AbstractCGroupElement{N}
    sym::Symbol
    inv::Symbol
    group_rep::Symbol

    function CvGroupElement{N}(sym, inv, grep) where N
        new{N}(sym, inv, grep)
    end
end

struct CvGroup{N} <:AbstractCGroup{N}
    sym::Symbol
    elements::LittleDict{Symbol, Symbol}
    
    function CvGroup{N}() where N
        @assert N ∈ (2, 3, 4, 6)
        if N == 2
            els = LittleDict(
                :E => :E,
                :C21 => :C21,
                :σ1 => :σ1,
                :σ2 => :σ2
                )
        elseif N == 3
            els = LittleDict(
                :E => :E,
                :C31 => :C32,
                :C32 => :C31,
                :σ1 => :σ1,
                :σ2 => :σ2,
                :σ3 => :σ2
                )
        elseif N == 4
            els = LittleDict(
                :E => :E,
                :C41 => :C43,
                :C42 => :C42,
                :C43 => :C41,
                :σ1 => :σ1,
                :σ2 => :σ2,
                :σ1d => :σ2,
                :σ2d => :σ2
            )
        elseif N == 6
            els = LittleDict(
                :E => :E,
                :C61 => :C65,
                :C62 => :C64,
                :C63 => :C63,
                :C64 => :C62,
                :C65 => :C61,
                :σ1 => :σ1,
                :σ2 => :σ2,
                :σ3 => :σ3,
                :σ1d => :σ1d,
                :σ2d => :σ2d,
                :σ3d => :σ3d
            )
        end

        return new{N}(Symbol("C$(N)v"), els)
    end

end

CvGroup(N) = CvGroup{N}()

function (p::CvGroup{N})(s::Symbol) where N
    return CvGroupElement{N}(s, p.elements[s], p.sym)
end

